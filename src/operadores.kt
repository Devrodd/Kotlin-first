fun main(args: Array<String>){

    var numero1 = 20
    var numero2 = 10

    println(numero1 + numero2) // somar
    println(numero1 - numero2) // subtracao
    println(numero1 / numero2) // divisao
    println(numero1 * numero2) // multiplicacao
    println(numero1 % numero2) // resto

    println(++numero1) // incremento
    println(--numero2) // decrement


}